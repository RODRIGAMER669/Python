# Operadores comparativos

print( 3 > 4)
print( 3 < 4)
print( 3 >= 4)
print( 3 <= 4)
print( 3 == 4)
print( 3 != 4)

print( "Hola" > "python")
print( "Hola" <= "python")
print( "Hola" >= "python")
print( "Hola" < "python")
print( "Hola" == "python")
print( "Hola" != "python")

print( "Hola" >= "Zola") # Ordenación alfabética por caracteres (min y mayus tambien)
print( "aaa" >= "aba")
print( len("aaa") >= len("aaa"))


# Operadores lógicos
print( 3 > 4 and "Hola" > "python")
print( 3 > 4 or "Hola" > "python")
print( not(3 > 4))